def remove_subscripts(text):
    subscript_map = {
        "₀": "0",
        "₁": "1",
        "₂": "2",
        "₃": "3",
        "₄": "4",
        "₅": "5",
        "₆": "6",
        "₇": "7",
        "₈": "8",
        "₉": "9",
    }
    for sub, normal in subscript_map.items():
        text = text.replace(sub, normal)
    return text
